package fitiuh.edu.vn.model;

public class BusInfo {
	
	private int busID;
	private String busName;
	private String activityType;
	private String distance;
	private String runingTime;
	private String spacingTime;
	private String busType;
	private String upTime;
	private String nTrip;
	private String pathOutward;
	private String pathHomeward;
	
	public int getBusID() {
		return busID;
	}
	public void setBusID(int busID) {
		this.busID = busID;
	}
	public String getBusName() {
		return busName;
	}
	public void setBusName(String busName) {
		this.busName = busName;
	}
	public String getActivityType() {
		return activityType;
	}
	public void setActivityType(String activityType) {
		this.activityType = activityType;
	}
	public String getDistance() {
		return distance;
	}
	public void setDistance(String distance) {
		this.distance = distance;
	}
	public String getRuningTime() {
		return runingTime;
	}
	public void setRuningTime(String runingTime) {
		this.runingTime = runingTime;
	}
	public String getSpacingTime() {
		return spacingTime;
	}
	public void setSpacingTime(String spacingTime) {
		this.spacingTime = spacingTime;
	}
	public String getBusType() {
		return busType;
	}
	public void setBusType(String busType) {
		this.busType = busType;
	}
	public String getUpTime() {
		return upTime;
	}
	public void setUpTime(String upTime) {
		this.upTime = upTime;
	}
	public String getnTrip() {
		return nTrip;
	}
	public void setnTrip(String nTrip) {
		this.nTrip = nTrip;
	}
	public String getPathOutward() {
		return pathOutward;
	}
	public void setPathOutward(String pathOutward) {
		this.pathOutward = pathOutward;
	}
	public String getPathHomeward() {
		return pathHomeward;
	}
	public void setPathHomeward(String pathHomeward) {
		this.pathHomeward = pathHomeward;
	}
	

}
